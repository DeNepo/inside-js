'use strict';

/* string to lower case

  converting your strings to all lower case letters
  is the simplest way to have the best includes results

  then you only need to includes for

*/

// store a couple strings in a variable for demonstration

console.log('--- to lower case ---');
console.log('abcd'.toLowerCase());
console.log('ABCD'.toLowerCase());

console.log('--- case insensitive search ---');

console.log('abcd'.toLowerCase().includes('b'));
console.log('ABCD'.toLowerCase().includes('b'));
console.log('abcd'.toLowerCase().includes('cd'));
console.log('ABCD'.toLowerCase().includes('cd'));
